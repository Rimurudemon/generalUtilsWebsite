export const Sidebar: React.FC = () => {
  const { logout, user } = useAuth();
  const { theme, toggleTheme } = useTheme();
  const navigate = useNavigate();

  const handleLogout = () => {
    logout();
    navigate('/login');
  };

  const navItems = [
    { to: '/pdf-tools', icon: <PdfIcon />, tooltip: 'PDF Tools' },
    { to: '/image-converter', icon: <ImageIcon />, tooltip: 'Image Converter' },
    { to: '/notes', icon: <CalendarIcon />, tooltip: 'Notes & Calendar' },
    { to: '/cgpa', icon: <CalculatorIcon />, tooltip: 'CGPA Calculator' },
    { to: '/profile', icon: <ProfileIcon />, tooltip: 'Profile' },
  ];

  return (
    <aside className="sidebar">
      <div className="sidebar-logo">P</div>
      
      <nav className="sidebar-nav">
        {navItems.map(item => (
          <NavLink
            key={item.to}
            to={item.to}
            className={({ isActive }) => `sidebar-link ${isActive ? 'active' : ''}`}
            data-tooltip={item.tooltip}
          >
            {item.icon}
          </NavLink>
        ))}
      </nav>

      <div className="sidebar-footer">
        <button
          className="theme-toggle"
          onClick={toggleTheme}
          title={theme === 'dark' ? 'Switch to Paper Theme' : 'Switch to Dark Theme'}
        >
          {theme === 'dark' ? <SunIcon /> : <MoonIcon />}
        </button>
        
        {user && (
          <button
            className="sidebar-link"
            onClick={handleLogout}
            data-tooltip="Logout"
          >
            <LogoutIcon />
          </button>
        )}
      </div>
    </aside>
  );
};
